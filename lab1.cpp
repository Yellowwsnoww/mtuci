// Функция преобразования IP-адреса из строки в 32-битное беззнаковое целое
uint32_t ipToUint(const string& ip) {
    uint32_t result = 0;
    size_t start = 0;
    size_t end = ip.find('.');
    int shift = 24; // старший октет сдвигается на 24 бита

    while (end != string::npos) {
        string octet = ip.substr(start, end - start);
        result |= (stoul(octet) << shift);
        shift -= 8;
        start = end + 1;
        end = ip.find('.', start);
    }
    // последний октет
    string octet = ip.substr(start);
    result |= stoul(octet);

    return result;
}

// Функция для получения двоичного представления 32-битного числа с точками между октетами
string uintToBinaryString(uint32_t num) {
    string binary;
    for (int i = 31; i >= 0; --i) {
        binary += ((num >> i) & 1) ? '1' : '0';
        if (i % 8 == 0 && i != 0) {
            binary += '.';
        }
    }
    return binary;
}

